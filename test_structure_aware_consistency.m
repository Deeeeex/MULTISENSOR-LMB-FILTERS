function test_structure_aware_consistency()
% TEST_STRUCTURE_AWARE_CONSISTENCY - Regression tests for structure-aware decoupled weighting

clc;
setPath;

fprintf('Test 1: spatial branch downweights posterior outlier\n');
[gaWeights, ~, debug] = computeAdaptiveFusionWeights( ...
    buildSpatialOutlierDistributions(), cell(3, 1), buildModelConfig(), 1, struct(), struct());
fprintf('  spatial weights:   %s\n', mat2str(debug.gaSpatialWeights, 4));
fprintf('  existence weights: %s\n', mat2str(debug.gaExistenceWeights, 4));
assert(debug.gaSpatialWeights(3) < debug.gaSpatialWeights(1));
assert(debug.gaSpatialWeights(3) < debug.gaSpatialWeights(2));
assert(numel(gaWeights) == 3);

fprintf('Test 2: existence branch downweights existence-disagreement outlier\n');
[~, ~, debug] = computeAdaptiveFusionWeights( ...
    buildExistenceOutlierDistributions(), cell(3, 1), buildModelConfig(), 1, struct(), struct());
fprintf('  spatial weights:   %s\n', mat2str(debug.gaSpatialWeights, 4));
fprintf('  existence weights: %s\n', mat2str(debug.gaExistenceWeights, 4));
assert(debug.gaExistenceWeights(3) < debug.gaExistenceWeights(1));
assert(debug.gaExistenceWeights(3) < debug.gaExistenceWeights(2));

fprintf('All structure-aware consistency tests passed.\n');
end

