function distributions = buildSpatialOutlierDistributions()
distributions = cell(1, 3);
distributions{1} = buildObjectArray([0, 0; 10, 0], [0.95, 0.15]);
distributions{2} = buildObjectArray([0.4, 0.2; 10.3, -0.1], [0.93, 0.12]);
distributions{3} = buildObjectArray([45, 0; 65, 0], [0.94, 0.14]);
end

function distributions = buildExistenceOutlierDistributions()
distributions = cell(1, 3);
distributions{1} = buildObjectArray([0, 0; 10, 0], [0.95, 0.10]);
distributions{2} = buildObjectArray([0.2, -0.1; 10.1, 0.1], [0.92, 0.12]);
distributions{3} = buildObjectArray([0.1, 0; 10.2, -0.2], [0.12, 0.92]);
end

function objects = buildObjectArray(positionArray, existenceArray)
numObjects = size(positionArray, 1);
template = struct( ...
    'r', 0.0, ...
    'numberOfGmComponents', 1, ...
    'w', 1, ...
    'mu', {{}}, ...
    'Sigma', {{}});
objects = repmat(template, 1, numObjects);
for idx = 1:numObjects
    state = [positionArray(idx, 1); positionArray(idx, 2); 0; 0];
    sigma = diag([4, 4, 1, 1]);
    objects(idx).r = existenceArray(idx);
    objects(idx).mu = {state};
    objects(idx).Sigma = {sigma};
end
end
